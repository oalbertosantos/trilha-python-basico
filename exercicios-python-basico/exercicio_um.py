#Converta as seguintes expressões matemáticas para que possam ser calculadas usando o interpretador Python: a) 10 + 20 x 30, b) 4²/30, c) (9**4 +2) x 6-1.

# a)

a = 10
b = 20
c = 30

operacao = (a + b * c)
resultado = operacao

